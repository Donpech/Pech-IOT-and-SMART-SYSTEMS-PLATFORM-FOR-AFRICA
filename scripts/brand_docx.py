def find_all_docx():
    """Find all DOCX files in the project that should be branded."""
    docx_files = []

    # Root-level proposal docs
    for f in PROJECT_ROOT.glob("*.docx"):
        docx_files.append(f)

    # AI Strategy docs
    ai_dir = PROJECT_ROOT / "ai_strategy"
    if ai_dir.exists():
        for f in ai_dir.glob("*.docx"):
            docx_files.append(f)

    # Brand templates
    brand_dir = PROJECT_ROOT / "brand_templates"
    if brand_dir.exists():
        for f in brand_dir.glob("*.docx"):
            docx_files.append(f)

    # Business documents
    biz_dir = PROJECT_ROOT / "business_documents"
    if biz_dir.exists():
        for f in biz_dir.glob("*.docx"):
            docx_files.append(f)

    # Contracts
    contracts_dir = PROJECT_ROOT / "contracts"
    if contracts_dir.exists():
        for f in contracts_dir.glob("*.docx"):
            docx_files.append(f)

    return sorted(docx_files)


