"""
PECH Group Holdings Ltd — DOCX Branding Post-Processor

Applies PECH brand colors, styled headers, colored dividers, and table formatting
to Word documents generated by pandoc (which strips all HTML styling).

Usage:
    python3 scripts/brand_docx.py                    # Process ALL docx files
    python3 scripts/brand_docx.py path/to/file.docx  # Process a single file

Brand Colors:
    Sky Blue:    #00BFFF  (primary)
    Orange:      #F5A623  (secondary/accent)
    Dark Blue:   #0099CC  (headings)
    Dark Orange: #E08A00  (H3 headings)
    Dark Navy:   #1B2838  (backgrounds)
"""

