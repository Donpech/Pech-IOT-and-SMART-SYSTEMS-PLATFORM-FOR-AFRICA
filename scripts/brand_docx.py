def style_headings(doc):
    """Apply brand colors to all headings in the document."""
    for paragraph in doc.paragraphs:
        style_name = paragraph.style.name if paragraph.style else ""

        if style_name == "Heading 1":
            # H1: Dark Blue text + orange bottom border
            set_all_runs_color(paragraph, DARK_BLUE)
            # Also set paragraph-level color for runs without explicit formatting
            _set_paragraph_font_color(paragraph, HEX_DARK_BLUE)
            set_paragraph_border_bottom(paragraph, HEX_ORANGE, size_eighths=18, space=2)
            # Make bold
            for run in paragraph.runs:
                run.font.bold = True
                run.font.size = Pt(22)

        elif style_name == "Heading 2":
            # H2: Dark Blue text + thin sky blue bottom border
            set_all_runs_color(paragraph, DARK_BLUE)
            _set_paragraph_font_color(paragraph, HEX_DARK_BLUE)
            set_paragraph_border_bottom(paragraph, HEX_SKY_BLUE, size_eighths=6, space=2)
            for run in paragraph.runs:
                run.font.bold = True
                run.font.size = Pt(16)

        elif style_name == "Heading 3":
            # H3: Dark Orange
            set_all_runs_color(paragraph, DARK_ORANGE)
            _set_paragraph_font_color(paragraph, HEX_DARK_ORANGE)
            for run in paragraph.runs:
                run.font.bold = True
                run.font.size = Pt(13)

        elif style_name == "Heading 4":
            # H4: Sky Blue
            set_all_runs_color(paragraph, SKY_BLUE)
            _set_paragraph_font_color(paragraph, HEX_SKY_BLUE)
            for run in paragraph.runs:
                run.font.bold = True
                run.font.size = Pt(11)


def _set_paragraph_font_color(paragraph, hex_color):
    """Set default font color at paragraph level (for text without explicit run formatting)."""
    pPr = paragraph._element.get_or_add_pPr()
    rPr = pPr.find(qn('w:rPr'))
    if rPr is None:
        rPr = parse_xml(f'<w:rPr {nsdecls("w")}></w:rPr>')
        pPr.append(rPr)
    color_elem = rPr.find(qn('w:color'))
    if color_elem is None:
        color_elem = parse_xml(f'<w:color {nsdecls("w")} w:val="{hex_color}"/>')
        rPr.append(color_elem)
    else:
        color_elem.set(qn('w:val'), hex_color)


def style_tables(doc):
    """Apply brand colors to all tables: blue header row, alternating row colors, borders."""
    for table in doc.tables:
        # Style the table borders
        tbl = table._tbl
        tblPr = tbl.find(qn('w:tblPr'))
        if tblPr is None:
            tblPr = parse_xml(f'<w:tblPr {nsdecls("w")}></w:tblPr>')
            tbl.insert(0, tblPr)

        # Set table borders
        borders_xml = (
            f'<w:tblBorders {nsdecls("w")}>'
            f'  <w:top w:val="single" w:sz="4" w:space="0" w:color="{HEX_BORDER_LIGHT}"/>'
            f'  <w:left w:val="single" w:sz="4" w:space="0" w:color="{HEX_BORDER_LIGHT}"/>'
            f'  <w:bottom w:val="single" w:sz="4" w:space="0" w:color="{HEX_BORDER_LIGHT}"/>'
            f'  <w:right w:val="single" w:sz="4" w:space="0" w:color="{HEX_BORDER_LIGHT}"/>'
            f'  <w:insideH w:val="single" w:sz="4" w:space="0" w:color="{HEX_BORDER_LIGHT}"/>'
            f'  <w:insideV w:val="single" w:sz="4" w:space="0" w:color="{HEX_BORDER_LIGHT}"/>'
            f'</w:tblBorders>'
        )
        existing_borders = tblPr.find(qn('w:tblBorders'))
        if existing_borders is not None:
            tblPr.remove(existing_borders)
        tblPr.append(parse_xml(borders_xml))

        # Style rows
        for row_idx, row in enumerate(table.rows):
            if row_idx == 0:
                # Header row: dark blue background, white text
                for cell in row.cells:
                    # Remove any existing shading
                    tcPr = cell._tc.get_or_add_tcPr()
                    existing_shd = tcPr.find(qn('w:shd'))
                    if existing_shd is not None:
                        tcPr.remove(existing_shd)
                    set_cell_shading(cell, HEX_DARK_BLUE)
                    set_cell_text_color(cell, WHITE)
                    # Make header text bold
                    for p in cell.paragraphs:
                        for run in p.runs:
                            run.font.bold = True
                            run.font.size = Pt(10)
            else:
                # Alternating rows
                bg_color = HEX_LIGHT_BLUE if row_idx % 2 == 0 else HEX_WHITE
                for cell in row.cells:
                    tcPr = cell._tc.get_or_add_tcPr()
                    existing_shd = tcPr.find(qn('w:shd'))
                    if existing_shd is not None:
                        tcPr.remove(existing_shd)
                    set_cell_shading(cell, bg_color)
                    set_cell_text_color(cell, BODY_TEXT)
                    for p in cell.paragraphs:
                        for run in p.runs:
                            run.font.size = Pt(9.5)


def insert_dividers_before_h2(doc):
    """
    Insert branded colored dividers (horizontal rules) before each H2 heading.
    This replaces the markdown --- dividers that pandoc strips out.
    """
    body = doc.element.body
    # Collect H2 elements first (to avoid modifying during iteration)
    h2_elements = []
    for p in doc.paragraphs:
        if p.style and p.style.name == "Heading 2":
            h2_elements.append(p._element)

    for elem in h2_elements:
        insert_branded_divider(doc, elem, style="double")


def style_blockquotes(doc):
    """Style blockquotes (Block Text style) with orange left border and light background."""
    for paragraph in doc.paragraphs:
        if paragraph.style and paragraph.style.name == "Block Text":
            pPr = paragraph._element.get_or_add_pPr()
            # Add orange left border
            pBdr = pPr.find(qn('w:pBdr'))
            if pBdr is None:
                pBdr = parse_xml(f'<w:pBdr {nsdecls("w")}></w:pBdr>')
                pPr.append(pBdr)
            left = parse_xml(
                f'<w:left {nsdecls("w")} w:val="single" w:sz="24" '
                f'w:space="6" w:color="{HEX_ORANGE}"/>'
            )
            existing = pBdr.find(qn('w:left'))
            if existing is not None:
                pBdr.remove(existing)
            pBdr.append(left)

            # Light yellow background
            shd = parse_xml(
                f'<w:shd {nsdecls("w")} w:fill="FFF8E1" w:val="clear"/>'
            )
            existing_shd = pPr.find(qn('w:shd'))
            if existing_shd is not None:
                pPr.remove(existing_shd)
            pPr.append(shd)


