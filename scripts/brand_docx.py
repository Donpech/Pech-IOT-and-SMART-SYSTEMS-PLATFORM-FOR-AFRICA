        f'    <w:pBdr>'
        f'      <w:top w:val="single" w:sz="36" w:space="0" w:color="{HEX_ORANGE}"/>'
        f'      <w:bottom w:val="single" w:sz="12" w:space="1" w:color="{HEX_SKY_BLUE}"/>'
