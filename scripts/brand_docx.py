    print("=" * 65)
    print("  PECH DOCX Branding Post-Processor")
    print("  Applying brand colors, headers, dividers, and table styling")
    print("=" * 65)
    print()

    if len(sys.argv) > 1:
        # Process specific file(s)
        files = [Path(f) for f in sys.argv[1:]]
    else:
        # Process all DOCX files
        files = find_all_docx()

    if not files:
        print("No DOCX files found to process.")
        return

    print(f"Found {len(files)} DOCX files to brand.\n")

    success = 0
    failed = 0
    for f in files:
        if brand_docx(f):
            success += 1
        else:
            failed += 1

    print()
    print("=" * 65)
    print(f"  Done! {success} branded, {failed} failed/skipped")
    print("=" * 65)


